
public class Main {

	public static void main(String[] args) {
		// for
        for (int i=1;i<10;i+=2) {
        	System.out.println(i);
        	
        }
        System.out.println("The For Loop is over ");
        
        
        //While
        int a=3;
        while ( a<10) {
        	System.out.println(a);
        	a+=1;
        	
        }
        System.out.println("The While Loop is over ");
        
   }

}
